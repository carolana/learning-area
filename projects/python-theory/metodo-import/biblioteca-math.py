#Para importar determinada biblioteca, é preciso usar o método IMPORT:
    #import math (aqui, será importada TODA a biblioteca math)
    #algumas funções da biblioteca math:
        #- sqrt (raiz quadrada)
        #- ceil (arredondar para cima)
        #- floor (arredondar para baixo)
        #- pow (função de exponenciação)
        #- factorial (fatorial)
        #- trunc (truncar o número)
#Porém, se não quiser fazer de todas as funções desta biblioteca, pode-se usar o método FROM:
    #from math import sqrt #aqui está importando apenas a função sqrt (raiz quadrada) da biblioteca math

#Caso queira importar mais de uma função e menos do que todas, apenas adicione uma vírgula
    #from math import sqrt,ceil
#Quando for usar o método from/import, não é preciso referenciar a biblioteca math na hora de usá-lo
