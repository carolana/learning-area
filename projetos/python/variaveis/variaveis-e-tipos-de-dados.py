#Variável é um nome que se refere a um valor
#Atribuição é o processo de criar uma nova variável e dar um novo valor a ela. Alguns exemplos de atribuições
#Nomes de variáveis podem ter o tamanho que você achar necessário e podem conter tanto letras como números, porém não podem começar com números.
#Para descobrir o tipo de uma variavel, é só usar o comando type(x)


#tipo de variavel = Inteiro (numérico initeiro)
variavel1 = 3
print(variavel1)
type(variavel1)

#tipo de variavel = Real (numérico flutuante)
variavel2 = 3.5
print(variavel2)
type(variavel2)

#tipo de variavel = String (textual)
variavel3 = "Aprendendo Pyton"
print(variavel3)
type(variavel3)

#tipo de variavel = Boolean (verdadeiro ou falso)
variavel4 = True
print(variavel4)
type(variavel4)

variavel5 = False
print(variavel5)
type(variavel5)